# Utilize um loop while e um loop for para contar de 0 até o número que o usuário digitar:

# ------------------------------------------ ESCREVA SEU CÓDIGO ABAIXO -----------------------------------------------------------

# LOOP WHILE

fim=int(input('Digite um numero: '))
contador = 0
while contador <= fim:
    print(contador)
    contador = contador + 1


# LOOP FOR

contador=int(input('Digite o numero desejado:'))
for contador in range(contador +1):
    print(contador )





